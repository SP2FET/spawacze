uint16_t POT_GetValue(uint8_t reg)
{
	uint8_t cmd;
	uint8_t tmp[2];

	cmd = ((reg&0x0F)<<4)|0x0C;

	HAL_I2C_Master_Transmit(&hi2c2,POT1_ADDR,&cmd,1,100);
	//HAL_I2C_Master_Receive(&hi2c2,POT1_ADDR,tmp,2,100);
	HAL_I2C_Mem_Read(&hi2c2,POT1_ADDR,cmd,I2C_MEMADD_SIZE_8BIT,tmp,2,100);
	return (tmp[1]<<8)|tmp[0];
}

